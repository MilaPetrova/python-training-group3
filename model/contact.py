class Contact:
    def __init__(self, firstname=None, lastname=None, nickname=None, title=None, company=None, address=None, home=None, email=None, id=None):
        self.firstname = firstname
        self.lastname = lastname
        self.nickname = nickname
        self.title = title
        self.company = company
        self.address = address
        self.home = home
        self.email = email
        self.id = id

